// NPM
import { onMounted, onUnmounted } from "vue";

/**************************************************************************
 * TYPES
 * ************************************************************************* */

type Setter = (mounted: boolean) => void;

/**************************************************************************
 * COMPOSABLE
 * ************************************************************************* */

function useInterfaceMounted(setter: Setter): void {
  // --> LIFECYCLE <--

  onMounted(() => {
    setter(true);
  });

  onUnmounted(() => {
    setter(false);
  });
}

/**************************************************************************
 * EXPORTS
 * ************************************************************************* */

export { useInterfaceMounted };
